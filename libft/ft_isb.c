int	ft_isb(const char *s)
{
	int i;

	i = 0;
	if (s[0] != '\0')
	{
		while (s[i])
		{
			if (s[i] != ' ' && s[i] != '\t' && s[i] != '\v')
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
