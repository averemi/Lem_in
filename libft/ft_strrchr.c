char		*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	i--;
	if (c != 0)
	{
		while (i >= 0)
		{
			if (s[i] == (char)c)
				return ((char *)(s + i));
			i--;
		}
	}
	else
		return ((char *)(s + i + 1));
	return (0);
}
