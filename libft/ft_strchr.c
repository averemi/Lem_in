char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	if (c != 0)
	{
		while (s[i] != '\0')
		{
			if (s[i] == (unsigned char)c)
				return ((char*)(s + i));
			i++;
		}
	}
	else if (c == 0)
	{
		i = 0;
		while (s[i] != '\0')
			i++;
		return ((char*)(s + i));
	}
	return (0);
}
